#include "push_swap.h"

int	main(int argc, char **argv)
{
	t_stack_node	*a;
	t_stack_node	*b;

	a = NULL;
	b = NULL;
	if (argc < 2)
		ft_error();
	init_stack_a(&a, argv + 1);
	if (!stack_sorted(a))
	{
		if (stack_len(a) == 2)
			sa(&a, false);
	}
	return (0);
}

void	ft_error()
{
	write(2, "Error", 5);
	write(2, "\n", 1);
	exit (1);
}
























